#homework 5th

example = ["a", "b", "c", "d", "e"]

# indexing - სიის ელემენტებზე წვდომა ნომრით (ინდექსით), იწყება 0-დან
print(example[0])  # გამოყავს 'a' - პირველი ასო
print(example[2])  # გამოყავს 'c' - მესამე ქსო

# ინდექსის გამოყენება შეგვიძლია სიის ნაწილის მისაღებადაც (slicing)
print(example[1:4])  # ['b', 'c', 'd'] => 1-დან 3 ინდექსამდე (4 არ ითვლება)

# უარყოფითი ინდექსის გამოყენებისას თვლა ბოლოდან იწყება
print(example[-1])  # 'e' - ბოლო ელემენტი


