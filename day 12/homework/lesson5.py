#1. + (დამატება):

a = 10
b = 5

# მაგალითები
print(a + b)       # 15
print(a + 20)      # 30
print(b + (-3))    # 2
print(a + b + 50)  # 65
print(100 + 200)   # 300


#2. - (გამოკლება):

x = 20
y = 7

# მაგალითები
print(x - y)       # 13
print(x - 15)      # 5
print(y - 10)      # -3
print(50 - x)      # 30
print(x - x)       # 0


#3. * (გამრავლება):

p = 6
q = 4

# მაგალითები
print(p * q)       # 24
print(p * 3)       # 18
print(q * (-2))    # -8
print(10 * p)      # 60
print(p * p)       # 36


#4. / (განაყოფი):

m = 25
n = 5

# მაგალითები
print(m / n)       # 5.0
print(m / 2)       # 12.5
print(n / 2)       # 2.5
print(100 / m)     # 4.0
print(m / m)       # 1.0



#5. ** (გადიდება - ხარისხი)


c = 3
d = 4

# მაგალითები
print(c ** d)      # 81 (3⁴)
print(c ** 2)      # 9
print(2 ** 5)      # 32
print(d ** 0)      # 1
print(c ** 1)      # 3

