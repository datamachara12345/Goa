# მომხმარებლის მიერ რიცხვების შეყვანა
num1 = float(input("შეიყვანეთ პირველი რიცხვი (num1): "))
num2 = float(input("შეიყვანეთ მეორე რიცხვი (num2): "))

# შედარების ოპერატორების გამოყენება და შედეგების დაბეჭდვა
print(f"{num1} > {num2} =", num1 > num2)    # მეტია
print(f"{num1} < {num2} =", num1 < num2)    # ნაკლებია
print(f"{num1} >= {num2} =", num1 >= num2)  # მეტია ან ტოლი
print(f"{num1} <= {num2} =", num1 <= num2)  # ნაკლებია ან ტოლი
print(f"{num1} == {num2} =", num1 == num2)  # ტოლია
print(f"{num1} != {num2} =", num1 != num2)  # არ არის ტოლი
