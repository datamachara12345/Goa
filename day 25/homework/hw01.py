def combine_texts(text1, text2):
    return text1 + text2
print(combine_texts("Hello", "World")) 



def repeat_text(text, number):
    return text * number
print(repeat_text("Hi", 3))



def greet(name):
    return f"გამარჯობა, {name}"
print(greet("დათა"))  



def words(words):
    return f"{words[0]}, {words[1]} და {words[2]}"
print(words(["ლომი", "ვეფხვი", "დათვი"]))  